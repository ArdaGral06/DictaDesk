def safe_lang(value: str) -> str:
    return "en" if value == "en" else "tr"


def t(ui_lang: str, key: str, **kwargs) -> str:
    ui_lang = safe_lang(ui_lang)
    text = TEXT.get(ui_lang, {}).get(key, TEXT["en"].get(key, key))
    return text.format(**kwargs)


def fail_reason_text(ui_lang: str, reason: str | None, **kwargs) -> str:
    raw = str(reason or "unknown").strip()
    base = raw.split(":")[0].lower()
    key = f"fail_reason_{base}"
    extra = dict(kwargs)
    if base == "run_code_failed" and ":" in raw and "code" not in extra:
        extra["code"] = raw.split(":", 1)[1].strip() or "?"
    translated = t(ui_lang, key, **extra)
    if translated == key:
        return raw or t(ui_lang, "fail_reason_unknown")
    return translated
